# All characters are stored in the memory as numbers.
# The ord() function returns the Unicode code point of a given character.
print (ord("A"))  # Output: 65
print (ord("1")) # Output: 49

print("-" * 20)
# The chr() function returns the character that represents a given Unicode code point.
print (chr(65))  # Output: 'A'
print (chr(49))  # Output: '1'